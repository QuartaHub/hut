<p>
    6.2.3. Бумажные носители документов при обезличивании персональных данных уничтожаются.
</p>
<p>
    6.2.4. Уничтожение персональных данных Клиента подразумевает прекращение какого-либо доступа к персональным данным Клиента.
</p>
<p>
    6.2.5. При уничтожении персональных данных Клиента работники Группы компаний «Оружейный квартал» не могут получить доступ к персональным данным субъекта в информационных системах.
</p>
<p>
    6.2.6. Бумажные носители документов при уничтожении персональных данных уничтожаются, персональные данные в информационных системах обезличиваются. Персональные данные восстановлению не подлежат.
</p>
<p>
    6.2.7. Операция уничтожения персональных данных необратима.
</p>
