</p>
<p>
    7.1.1. Под передачей персональных данных субъекта понимается распространение информации по каналам связи и на материальных носителях. При передаче персональных данных работники юридических лиц Группы компаний «Оружейный квартал» должны соблюдать следующие требования:
</p>
<p>
    7.1.2. Не сообщать персональные данные Клиента в коммерческих целях.
</p>
<p>
    7.1.3. Не сообщать персональные данные Клиента третьей стороне без письменного согласия Клиента, за исключением случаев, установленных федеральным законом РФ.
</p>
<p>
    7.1.4. Предупредить лиц, получающих персональные данные Клиента о том, что эти данные могут быть использованы лишь в целях, для которых они сообщены, и требовать от этих лиц подтверждения того, что это правило соблюдено.
</p>
<p>
    7.1.5. Разрешать доступ к персональным данным Клиентов только специально уполномоченным лицам, при этом указанные лица должны иметь право получать только те персональные данные Клиентов, которые необходимы для выполнения конкретных функций.
</p>
<p>
    7.1.6. Осуществлять передачу персональных данных Клиента в пределах юридических лиц Группы компаний «Оружейный квартал» в соответствии с настоящим Положением, нормативно- технологической документацией и должностными инструкциями.
</p>
<p>
    7.1.7. Предоставлять доступ Клиента к своим персональным данным при обращении либо при получении запроса Клиента. Группа компаний «Оружейный квартал» обязана сообщить Клиенту информацию о наличии персональных данных о нем, а также предоставить возможность ознакомления с ними в течение десяти рабочих дней с момента обращения.
</p>
<p>
    7.1.8. Передавать персональные данные Клиента представителям Клиента в порядке, установленном законодательством и нормативно-технологической документацией и ограничивать эту информацию только теми персональными данными субъекта, которые необходимы для выполнения указанными представителями их функции.
</p>
<p>
    7.2. Хранение и использование персональных данных:
</p>
<p>
    7.2.1. Под хранением персональных данных понимается существование записей в информационных системах и на материальных носителях.
</p>
<p>
    7.2.2. Персональные данные Клиентов обрабатываются и хранятся в информационных системах, а также на бумажных носителях в Группе компаний «Оружейный квартал». Персональные данные Клиентов также хранятся в электронном виде: в локальной компьютерной сети Группы компаний «Оружейный квартал», в электронных папках и файлах в ПК Генерального директора и работников, допущенных к обработке персональных данных Клиентов.
</p>
<p>
    7.2.3. Хранение персональных данных Клиента может осуществляться не дольше, чем этого требуют цели обработки, если иное не предусмотрено федеральными законами РФ.
</p>
<p>
    7.3. Сроки хранения персональных данных:
</p>
